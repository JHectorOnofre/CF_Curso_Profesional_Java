package M2TiposDeDatos;

public class Variables {

    public static void main(String[] args) {

        /*
        * 4 tipos de datos
        * Enteros
        * Decimales
        * Caracteres
        * Booleanos
        */
       
        // Enteros
        int version; 
        version = 9;
        System.out.println(version);
   
        int versionJava = 11;
        System.out.println(versionJava);

        //float
        float versionEspecifica = 11.2f;
        System.out.println(versionEspecifica);

        //Char
        char letra = 'v';
        char numero = 97; //ASCII

        System.out.println(letra);
        System.out.println(numero);

        boolean valor1 = true;
        boolean valor2 = false;


    }

}
